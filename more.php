        <h4>Economic activity status, by age and sex</h4>
        <p>The 2010 PHC reported a total population of 207,071 for the 
          district out of which 124,105 are persons, 15 years and older,
          representing 59.9 percent of the total population who formed the
          labour force of the district. Out of this figure, 79,199 representing
          63.8% are employed whilst 6,476 are unemployed and 38,430 (about 31%)
        are economically inactive labour force.</p>
        <p>The nature of the distribution does not differ markedly between males and females.
          Both males and females have the same peaked of the employed population to be 25 - 29
        years age group recording 16.2 percent and 17 percent respectively. The employed category 
        leads with the highest in the economic activity status with 63.8 percent. It is followed by
        economically not active with 31 percent. The category with the lowest rate is the unemployed 
      with 5.2 percent rate.</p>
