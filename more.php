      <h4>Governance:</h4>
      <p>This section discusses the role of the District Assembly and its substructures, Decentralized
      Departments, Area Councils, Unit Committees, Civil Societies, NGO's, Donor communities, Traditional
      authorities and other stakeholders in the development process. It also discusses the District Assemblies 
      Finances. District Assemblies have been assigned functions that adequately empower them to provide 
      de-concerntrated and devolved local public services and to be responsible for the overall development of the districts
      and Gomoa Central District Assembly is not an exception</p>
       </div>
       </div>


       <div class = "row">
        <div class = "col-md-12">
       <h4>Governance Issues (Functions and structure)</h4>
       <p>By law, the Gomoa Central District Assembly established by Legislative Instrument 1883 in 2008, constitutes
        the highest political and administrative authority in the district (Local Government Act 1993, Act 462) with the
        mandate to initiate and co-ordinate all development efforts and to implement government policies aimed at 
        sustainable development at all levels. This is simply captured in the Gomoa Central District Assembly's 
        Mission Statement. 
       </p>
    </div>
    </div>

    <div class = "row">
        <div class = "col-md-12">
       <h4>General Assembly </h4>
       <p>The General Assembly of the Gomoa Central District is composed of forty-one (41) members made up of 27 elected
       members and 14 appointees (including the Member of Parliament and the District Chief Executive).</h6></p>
       <p>The Assembly is empowered to exercise deliberative, legislative and executive functions in the district.
        The presiding Member who is elected from among the Assembly Members chairs the Assembly meetings as well as
        the Complaints sub-committee.
       </p>
    </div>
    </div>
    
    <div class = "row">
        <div class = "col-md-12">
       <h4>Decentralized Departments </h4>
       <p>The Local Government (Act 462) makes provision for the establishment of Eleven (11) Decentralized Departments
        (now Departments of the Assembly) and such administration sub-structures as Urban / Town  Area Councils and 
        Unit committies to facilitate the process of local governance.</p>
        <p>The central administration, which provides secretarial and advisory services to the Assembly in its planning,
          programming and budgeting functions, is headed by the District C0-ordinating Director.
        </p>
        <p>
          The District Co-ordinating Director also has an oversight responsibility on the other decentralized departments.
        </p>
       <p>The District is divided into seven (7) Area councils namely: </p><br/>
        <li>Afransi</li><br/>
        <li>Aboso</li><br/>
        <li>Ekwamkrom</li><br/>
        <li>Asebu / Pomadze</li><br/>
        <li>Ojobi</li><br/>
        <li>Nyanyano</li><br/>
        <li>Dominase</li><br/>
      <p>
        The district Planning Coordinating Unit (DPCU) however, advises and provides a secretariat for the District as a 
        planning authority in its planning, programming, monitoring, evaluation and coordinating functions.<br/>
        The Executive Committee co-ordinating plans the programs of the sub-committees and submit these as comprehensive 
        plans of action to the District Assembly. The Executive Committee which excludes the presiding Members (PM) is
        chaired by the District Chief Executive (DCE).
      </p> 
      <p>This body performs the executive and administrative functions of the District Assembly. The executive committee
        is made up of the chairpersons of the various sub-committees of the assembly. The sub - committees include:<br/>
      </p>
        <li>Finance and Administration</li><br/>
        <li>Justice and Security</li><br/>
        <li>Social Services</li><br/>
        <li>Works</li><br/>
        <li>Development Planning</li><br/>
       <p> Apart from these five statutory committees, the district has three other sub-committees namely:<br/></p>
        <li>Production and Gainful Employment (SPGE)</li><br/>
        <li>Agriculture</li><br/>
        <li>Education</li><br/>
