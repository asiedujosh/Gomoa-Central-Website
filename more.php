        <h4>Social and Culture Structure</h4>
        <p>The culture of the people in the district is seen in their way of life which
          distinguishes them from people in other districts. This is expressed in their 
          Dressing, languages spoken, festivals, art and craft, food, religious belief and
          occupation among others. According to the 2010 PHC the district is inhabited mainly
          by people of Gomoa origin who form over 95 percent of the population with the remaining 
          8 percent coming from Northern Ghana, the Volta region and other Akan ethnic groups.
          </p>
          <p>The communal spirit among the people is seen in the corporate as well as individual 
          responsibilities of members in the communities. Members in a community attend and participate 
          in ceremonies such as marriages, funerals and christening.
          Participants at these ceremonies make donations as their contribution to the host to pay expenses 
          incurred. Ocassionally, communal work is organized to do clean-ups as well as construction of 
          other community projects.</p>
          <p>The communities are well organized and the Asafo groups (military wings) are noted for their 
          mobilization instincts. </p>
          </div>
