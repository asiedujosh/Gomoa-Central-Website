      <h4>Background:</h4>
      <p>Gomoa Central District is one of the twenty (20) Districts in the Central Region of Ghana.</p>
      <p>The District was carved out as a separate district from the then Gomoa District in June 2008 by
      the Legislative instrument 1883</p>
      <p>It occupies an area of <b>539.69</b> square kilometers with a total projected 2016 population 
        of <b>240,581,</b> comprising <b>47.5</b> percent males and <b>52.7</b> percent females with
        <b>2.5</b> growth rate (2010 PHC). The district has population density of <b>384</b> persons per
         square kilometer.</p>
       </div>
       </div>


